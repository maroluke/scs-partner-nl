				<h1>Was bringt Ihnen All IP?</h1>

				<p>Durch die Umstellung der Telefonie auf das Internet Protokoll stehen Ihnen zahlreiche hilfreiche Funktionen zur Verfügung. Alle mit dem Ziel, den Arbeitsalltag zu erleichtern. Nutzen Sie die neuen Freiheiten und Chancen, die Ihnen IP ermöglicht.</p>
			</section>

			<section class="inner topic-item">
				<img src="img/image-01.png" alt="Immer und überall mobil." title="Immer und überall mobil.">

				<h2>Immer und überall mobil.</h2>

				<p>Mit IP-Telefonie sind Sie weder an Standort, Gerät oder Anwendung gebunden. Sie arbeiten von überall aus produktiv; sei es unterwegs, im Homeoffice oder im Ausland. Ihre herkömmliche Festnetznummer lässt sich auf jedes Gerät übertragen, das für Telefonie geeignet ist. Dank der HD-Sprachqualität macht niemand aus, ob Sie den Anruf mit einem Telefon, Ihrem Handy oder der Smartwatch entgegennehmen.</p>
			</section>

			<section class="inner topic-item">
				<img src="img/image-02.png" alt="Flexible Kanäle für jeden Anspruch." title="Flexible Kanäle für jeden Anspruch.">

				<h2>Flexible Kanäle für jeden Anspruch.</h2>

				<p>Sie können gerade nicht rangehen? Seien Sie dennoch erreichbar, indem Sie einfach Ihren Anwesenheitsstatus anpassen und währenddessen via Chat kontaktiert werden. Zusätzlich steht Ihnen eine Vielzahl anderer Kommunikationskanäle zur Verfügung wie Online-Meeting, Videotelefonie oder Bildschirmfreigabe. Sie wählen, welcher davon für die entsprechende Situation am besten geeignet ist.</p>
			</section>

			<section class="inner topic-item">
				<img src="img/image-03.png" alt="Sichere Datenverwaltung in der Cloud." title="Sichere Datenverwaltung in der Cloud.">

				<h2>Sichere Datenverwaltung in der Cloud.</h2>

				<p>Neben einwandfreier Kommunikation ist eine übersichtliche Datenverwaltung essenziell für effizientes Arbeiten. Dafür sorgt die Cloud, auf der Sie all Ihre wichtigen Dokumente abspeichern und wieder abrufen – sogar offline. So haben Sie auch ausserhalb Ihres Arbeitsplatzes immer die aktuellste Version zur Hand.</p>
			</section>
