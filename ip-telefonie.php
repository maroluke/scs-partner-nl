				<p class="intro">Die Schweiz stellt aktuell die alte Festnetztelefonie vollständig auf All IP um. Das bedeutet: Künftig nutzt das Telefonsignal die gleiche Technik wie das Internet; das Internet Protokoll – kurz IP.</p>

				<p>All IP ist der neue, weltweit bewährte Standard und Basis für unsere Kommunikation. Die veraltete Festnetztelefonie konnte mit den Anforderungen der Zeit nicht mehr mithalten. Die Digitalisierung der Kommunikation ist daher die logische Konsequenz.</p>
